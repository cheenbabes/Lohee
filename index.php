                    <p align="left">I thought long and hard about how to balance these three points: high quality, natural materials and inexpensive enough to afford. I was living in a yoga community and was connected with a family owned company in India. They do not weave by hand; they have electric looms. However, I found that this actually creates a fair price for the weavers. If the weavers were working by hand, they would get paid very little for their time or the rebozos would no longer be affordable for doulas. This was a good way to balance fairness and affordability. My husband and I did a great deal of background checking to insure that the small company we partnered with was upstanding, treated their employees fairly, and created quality fabric. I now work personally with the weaver to create the rebozos we have available.</p>

